using HrevertCRM.Data.ViewModels.Enumerations;
using HrevertCRM.Entities.Enumerations;

namespace HrevertCRM.Data.Mapper.Enumerations
{
    public class AccountTypeToAccountTypeViewModelMapper : MapperBase<AccountTypes, AccountTypeViewModel>
    {
        public override AccountTypes Map(AccountTypeViewModel viewModel)
        {
            return new AccountTypes
            {
                Id = viewModel.Id ?? 0,
                Value = viewModel.Value,
                CompanyId = viewModel.CompanyId,
                Active = viewModel.Active,
                Version = viewModel.Version
            };
        }

        public override AccountTypeViewModel Map(AccountTypes entity)
        {
            return new AccountTypeViewModel
            {
                Id = entity.Id,
                Value = entity.Value,
                CompanyId = entity.CompanyId,
                Active = entity.Active,
                Version = entity.Version
            };
        }
    }
}
