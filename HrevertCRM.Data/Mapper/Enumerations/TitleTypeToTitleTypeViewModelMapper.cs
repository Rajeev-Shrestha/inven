using System;
using HrevertCRM.Data.ViewModels.Enumerations;
using HrevertCRM.Entities.Enumerations;

namespace HrevertCRM.Data.Mapper.Enumerations
{
    public class TitleTypeToTitleTypeViewModelMapper : MapperBase<TitleTypes, TitleTypeViewModel>
    {
        public override TitleTypes Map(TitleTypeViewModel viewModel)
        {
            return new TitleTypes
            {
                Id = viewModel.Id ?? 0,
                Value = viewModel.Value,
                CompanyId = viewModel.CompanyId,
                Active = viewModel.Active,
                Version = viewModel.Version
            };
        }

        public override TitleTypeViewModel Map(TitleTypes entity)
        {
            return new TitleTypeViewModel
            {
                Id = entity.Id,
                Value = entity.Value,
                CompanyId = entity.CompanyId,
                Active = entity.Active,
                Version = entity.Version
            };
        }
    }
}
